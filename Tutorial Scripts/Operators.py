# Operators

# Arithmetic Operators
a1 = 5
a2 = 2
print(a1 + a2)  # Addition (Here, a1 and a2 are operands and + is operator)
print(a1 - a2)  # Subtraction
print(a1 * a2)  # Multiplication
print(a1 / a2)  # Division
print(a1 % a2)  # Modulus (remainder)
print(a1 ** a2)  # Exponentiation (a1 raise to the power a2)
print(a1 // a2)  # Floor division (rounded down to the nearest integer)

# Comparison Operators
x = 10
y = 5
print(x == y)  # Equal to
print(x != y)  # Not equal to
print(x > y)   # Greater than
print(x < y)   # Less than
print(x >= y)  # Greater than or equal to
print(x <= y)  # Less than or equal to

# Logical Operators
p = True
q = False
print(p and q)  # Logical AND
print(p or q)   # Logical OR
print(not p)     # Logical NOT

# Membership Operators
myList = [1, 2, 3, 4]
print(2 in myList)   # Membership (True)
print(5 not in myList)  # Not in (True)

# Identity Operators
x = [1, 2, 3]
y = [1, 2, 3]
print(x is y)    # Identity (False)
print(x is not y)  # Not identity (True)
